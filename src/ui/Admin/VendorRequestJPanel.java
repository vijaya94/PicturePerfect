    public void loadVendorsList(){
        
        try {
                    Connection connection = (Connection) DBConnection.con();
                    // Get vendors that need approval
                    PreparedStatement st = connection.prepareStatement("Select vendor_id, vendor_name,ol.org_type, concat(addr_line_1,\" \", addr_line_2) as address, email, phone_number from vendor_details vd left join organization_list ol on ol.org_id = vd.vendor_type_id  where registration_status=1");

                    ResultSet rs = st.executeQuery();
                    
                    
                    while(rs.next()){
                        String vendorId = rs.getString(1);
                      String vendorName = rs.getString(2);
                      String vendorType = rs.getString(3);
                      String address = rs.getString(4);
                      String email = rs.getString(5);
                      String phnNumber = rs.getString(6);
                      
                      
                      String tblData[] = {vendorId, vendorName, vendorType, address, email, phnNumber};
                      DefaultTableModel tblModel = (DefaultTableModel)tblAdminVendorRequests.getModel();
                      tblModel.addRow(tblData);
                    }
                    
                } catch (SQLException sqlException) {
                    sqlException.printStackTrace();
                } 
    }
    
