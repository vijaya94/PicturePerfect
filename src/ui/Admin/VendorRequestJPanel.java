    private void btnApproveVendorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnApproveVendorActionPerformed
        int rowSelected = tblAdminVendorRequests.getSelectedRow();
        if(rowSelected < 0){
            JOptionPane.showMessageDialog(this, "Select a record to approve.");
            return;
        }
        rowSelected = tblAdminVendorRequests.convertRowIndexToModel(rowSelected);
        DefaultTableModel model = (DefaultTableModel) tblAdminVendorRequests.getModel();
       
        Integer id = Integer.parseInt(model.getValueAt(rowSelected, 0).toString());
        
        try {
                    Connection connection = (Connection) DBConnection.con();
                    
                   // updating registration status to 3 (approved)
                    String insertTableSQL = "UPDATE vendor_details SET registration_status = 3 where vendor_id = ?;";
                    PreparedStatement st = (PreparedStatement)connection.prepareStatement(insertTableSQL);
                    st.setInt(1, id);
                                                          
                    Integer returnedValue = st.executeUpdate();
                    if (returnedValue>0) {
                        
                        JOptionPane.showMessageDialog(this, "Vendor is approved");
                    
        TableRowSorter<DefaultTableModel> tableRowSorter = new TableRowSorter(model);
        tblAdminVendorRequests.setRowSorter(tableRowSorter);
        tableRowSorter.setRowFilter(null);
                        
                        loadVendorsList();
                        
                    } else {
                        JOptionPane.showMessageDialog(this, "Something went wrong");
                    }
                    
                    
                } catch (SQLException sqlException) {
                    sqlException.printStackTrace();
                }
        
    
       
    }//GEN-LAST:event_btnApproveVendorActionPerformed

